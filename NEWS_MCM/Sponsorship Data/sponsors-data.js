function renderPatreonTiers() {
    const container = document.querySelector('#patreon-content .patrons-scroll-box');
    if (!container) return;

    container.innerHTML = '';

    const tiers = sponsorsData.patreon.tiers;
    const tierOrder = ['level4', 'level2', 'level1', 'former', 'free'];
    const tierClasses = {
        level4: 'tier-level-4',
        level2: 'tier-level-2',
        level1: 'tier-level-1',
        former: 'tier-former',
        free: 'tier-free'
    };

    tierOrder.forEach(tierKey => {
        const tier = tiers[tierKey];
        const div = document.createElement('div');
        div.className = `tier-group ${tierClasses[tierKey]}`;

        const h5 = document.createElement('h5');
        h5.className = 'tier-name';
        h5.textContent = `${tier.name} : ${tier.count}`;
        div.appendChild(h5);

        const namesDiv = document.createElement('div');
        namesDiv.className = 'patrons-names';

        tier.patrons.forEach(patron => {
            const span = document.createElement('span');
            let tooltip = '';

            if (tierKey === 'level4' || tierKey === 'level2' || tierKey === 'level1') {
                tooltip = `Lifetime: $${patron.lifetime.toFixed(2)}\nPatron since: ${patron.since}`;
                span.textContent = `${patron.name} ($${patron.monthly.toFixed(2)} Monthly)`;
            } else if (tierKey === 'former') {
                tooltip = `Patron from: ${patron.from}\nto: ${patron.to}`;
                span.textContent = patron.name;
            } else {
                tooltip = `Follower since: ${patron.since}`;
                span.textContent = patron.name;
            }

            span.setAttribute('data-tooltip', tooltip);

            const tierStyles = {
                level4: 'color: #fff7d6; background: rgba(212, 175, 55, 0.18); border: 1px solid rgba(212, 175, 55, 0.45); box-shadow: 0 0 12px rgba(212, 175, 55, 0.22);',
                level2: 'color: #d1fae5; background: rgba(34, 197, 94, 0.16); border: 1px solid rgba(34, 197, 94, 0.35);',
                level1: 'color: #e0f6ff; background: rgba(56, 189, 248, 0.16); border: 1px solid rgba(56, 189, 248, 0.35);',
                former: 'color: #fff4e3; background: rgba(230, 213, 184, 0.16); border: 1px solid rgba(230, 213, 184, 0.35);',
                free: 'color: #d1d5db; background: rgba(107, 114, 128, 0.12); border: 1px solid rgba(107, 114, 128, 0.30);'
            };

            if (tierStyles[tierKey]) {
                span.style.cssText = tierStyles[tierKey];
            }

            namesDiv.appendChild(span);
        });

        div.appendChild(namesDiv);
        container.appendChild(div);
    });
}

function renderKofiSupporters() {
    const container = document.querySelector('#kofi-content .patrons-names');
    if (!container) return;

    container.innerHTML = '';

    const supporters = sponsorsData.kofi.supporters;

    supporters.sort((a, b) => b.amount - a.amount);

    supporters.forEach(supporter => {
        const span = document.createElement('span');
        let tooltip = `Received: $${supporter.amount.toFixed(2)}`;
        if (supporter.message) {
            tooltip += `\nMessage: ${supporter.message}`;
        }
        tooltip += `\nDate: ${supporter.date}`;

        span.setAttribute('data-tooltip', tooltip);

        const displayName = supporter.alias || supporter.name;

        if (supporter.color) {
            span.style.cssText = `color: ${supporter.color};`;
            if (supporter.amount >= 50 || supporter.amount >= 20) {
                span.style.cssText += ' text-shadow: 0 0 8px rgba(255, 215, 0, 0.6); font-weight: bold;';
            } else if (supporter.amount >= 15) {
                span.style.cssText += ' font-weight: bold;';
            }
        } else {
            if (supporter.amount >= 50) {
                span.style.cssText = 'color: #ffd700; text-shadow: 0 0 8px rgba(255, 215, 0, 0.6); font-weight: bold;';
            } else if (supporter.amount >= 15) {
                span.style.cssText = 'color: #ffd700; text-shadow: 0 0 8px rgba(255, 215, 0, 0.6); font-weight: bold;';
            } else if (supporter.amount >= 5) {
                span.style.cssText = 'color: #ffcc33; font-weight: bold;';
            } else if (supporter.amount >= 3) {
                span.style.cssText = 'color: #d4e157;';
            } else {
                span.style.cssText = 'color: #43a047;';
            }
        }

        span.textContent = displayName;
        container.appendChild(span);
    });
}

function updatePatreonStats() {
    const badge = document.querySelector('#patreon-content .patron-count-badge');
    if (badge) {
        badge.textContent = `Patrons: ${sponsorsData.patreon.stats.total}`;
    }

    const pie3d = document.querySelector('#patreonPie3d');
    if (pie3d) {
        pie3d.setAttribute('data-free', sponsorsData.patreon.stats.free);
        pie3d.setAttribute('data-former', sponsorsData.patreon.stats.former);
        pie3d.setAttribute('data-active', sponsorsData.patreon.stats.active);
    }
}

function updateKofiStats() {
    const badge = document.querySelector('#kofi-content .patron-count-badge');
    if (badge) {
        badge.textContent = `Supporters: ${sponsorsData.kofi.stats.total}`;
    }

    const pie3d = document.querySelector('#kofiPie3d');
    if (pie3d) {
        pie3d.setAttribute('data-donors', sponsorsData.kofi.stats.total);
    }
}

function initializeSponsors() {
    updatePatreonStats();
    updateKofiStats();
    renderPatreonTiers();
    renderKofiSupporters();

    if (typeof initializePatronTooltips === 'function') {
        initializePatronTooltips();
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeSponsors);
} else {
    initializeSponsors();
}